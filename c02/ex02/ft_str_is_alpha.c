#include <stdio.h>
#include <unistd.h>

int		ft_str_is_alpha(char *str)
{
	int x;

	x = 0;
	while (str[x] != '\0')
	{
		if (!((str[x] <= 64 && str[x] >= 91)
					&& (str[x] <= 96 && str[x] >= 123)))
		{
			return (0);
		}
		x++;
	}
	return (1);
}

