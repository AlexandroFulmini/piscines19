#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int x =*a;
	*a = *b;
	*b = x;
}

int		main()
{
	int a;
	int b;
	b = 20;
	a = 10;
	ft_swap(a,b);
	return(0);
}
